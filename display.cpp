bool initDisplay(Adafruit_SH1107 * display)
{
    if (display->begin(0x3C, true)) /*Address 0x3C default*/
    {
        display->clearDisplay();
        display->setRotation(1);
        display->setTextSize(1);
        display->setTextColor(SH110X_WHITE);

        if (SPLASH_DISPLAY_TIME > 0)
        {
            for (size_t x = 0; x < SPLASH_DIMENSIONS; x++)
            {
                for (size_t y = 0; y < SPLASH_DIMENSIONS; y++)
                {
                    if (!splash[y][x])
                    {
                        display->drawPixel((SPLASH_X_OFFSET + x), (SPLASH_Y_OFFSET + y), SH110X_WHITE);
                    }
                }
            }
            display->display();
            delay(SPLASH_DISPLAY_TIME);
        }
        resetDisplay(display);
        display->display();

        return true;
    }
    return false;
}



void resetDisplay(Adafruit_SH1107* display)
{
    display->clearDisplay();
    display->setCursor(0, 0);
}
